#!/bin/bash
# =============================================================================
# AI Sales Operator — Update Script (обновление без полного переразворота)
# Запуск: sudo -u aisales bash scripts/update-vps.sh
# =============================================================================

set -euo pipefail

GREEN='\033[0;32m'
BLUE='\033[0;34m'
YELLOW='\033[1;33m'
NC='\033[0m'

info()    { echo -e "${BLUE}[INFO]${NC} $1"; }
success() { echo -e "${GREEN}[OK]${NC}   $1"; }
warn()    { echo -e "${YELLOW}[WARN]${NC} $1"; }

APP_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
cd "${APP_DIR}"

info "=== Обновление AI Sales Operator ==="
info "Директория: ${APP_DIR}"

# --------------------------------------------------------------------------
# 1. Получаем изменения
# --------------------------------------------------------------------------
info "Git pull..."
git pull --ff-only origin main

# --------------------------------------------------------------------------
# 2. Устанавливаем/обновляем зависимости (только если изменился package-lock)
# --------------------------------------------------------------------------
if git diff HEAD@{1} --name-only 2>/dev/null | grep -q "package-lock.json"; then
    info "package-lock.json изменился — обновляем npm зависимости..."
    npm ci --prefer-offline
    success "npm зависимости обновлены"
else
    info "package-lock.json не изменился — пропускаем npm ci"
fi

# --------------------------------------------------------------------------
# 3. Python зависимости
# --------------------------------------------------------------------------
if git diff HEAD@{1} --name-only 2>/dev/null | grep -q "pyproject.toml"; then
    info "pyproject.toml изменился — обновляем Python зависимости..."
    .venv/bin/pip install -q --upgrade fastapi uvicorn playwright pydantic aiohttp httpx requests pillow qrcode maxapi-python
    success "Python зависимости обновлены"
fi

# --------------------------------------------------------------------------
# 4. Сборка
# --------------------------------------------------------------------------
info "Собираем TypeScript / Vite..."
npm run build
success "Сборка завершена"

# --------------------------------------------------------------------------
# 5. Миграции БД
# --------------------------------------------------------------------------
info "Запускаем миграции базы данных..."
npx drizzle-kit push --force || warn "Миграции завершились с предупреждением"
success "Миграции выполнены"

# --------------------------------------------------------------------------
# 6. Перезапуск процессов
# --------------------------------------------------------------------------
info "Перезапускаем PM2 процессы..."
pm2 restart ecosystem.config.cjs --update-env
success "Процессы перезапущены"

pm2 status

echo ""
success "=== Обновление завершено ==="
